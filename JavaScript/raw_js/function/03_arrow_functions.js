/**
 * =========================================================================
 * FILE: 03_arrow_functions.js
 * TOPIC: ARROW FUNCTIONS (ES6 MODERN SYNTAX)
 * =========================================================================
 * * সুবিধা:
 * ১. কোড ছোট এবং ক্লিন হয়।
 * ২. 'this' কিউওয়ার্ড নিয়ে ঝামেলা কম হয় (এটি পরে শিখবো)।
 * ৩. এক লাইনের কোডে রিটার্ন কিউওয়ার্ড দরকার হয় না।
 */

// ১. বেসিক স্ট্রাকচার
const greet = (user) => {
    return "Hello " + user;
};

// ২. ইমপ্লিসিট রিটার্ন (Implicit Return)
// যদি এক লাইনের কাজ হয়, তবে {}, return এবং ; (ভেতরের) লাগবে না।
const double = (num) => num * 2;
console.log(double(5)); // ১০


// ৩. প্যারামিটার ভেরিয়েশন
const singleParam = name => "Mr. " + name; // একটি প্যারামিটার হলে () লাগে না
const noParam = () => console.log("Testing Arrow Function"); // প্যারামিটার না থাকলে () লাগবেই


/**
 * ৪. ছোট ডিটেইলস এবং সীমাবদ্ধতা:
 * - অ্যারো ফাংশন ও হোইস্ট (Hoisted) হয় না। একে আগে ডিক্লেয়ার করতে হবে।
 * - ইন্টারভিউ প্রশ্ন: "অ্যারো ফাংশনে কি arguments অবজেক্ট থাকে?"
 * উত্তর: না, অ্যারো ফাংশনের নিজস্ব 'arguments' অবজেক্ট থাকে না।
 */

// রিয়েল লাইফ ইউজ কেস (যেভাবে প্রজেক্টে ব্যবহার করবেন)
const isEven = num => num % 2 === 0;

console.log("Is 8 even?", isEven(8)); // true
console.log("Is 7 even?", isEven(7)); // false


/**
 * ৫. অ্যাডভান্স হ্যান্ডলিং: Rest Parameter (...args)
 * ব্যাখ্যা: যখন আমরা জানি না ইউজার কয়টি আর্গুমেন্ট পাঠাবে, তখন '...' ব্যবহার করে 
 * সবগুলোকে একটি 'Real Array' হিসেবে রিসিভ করা যায়। 
 * এটি অ্যারো ফাংশনে 'arguments' অবজেক্টের আধুনিক বিকল্প।
 */

const sumAll = (...numbers) => {
    // এখানে 'numbers' একটি আসল অ্যারে, তাই সব অ্যারে মেথড চালানো যাবে
    return numbers.reduce((acc, curr) => acc + curr, 0);
};

console.log(sumAll(10, 20, 30)); // ৬০
console.log(sumAll(5, 5));       // ১০



/**
 * ৬. ইমপ্লিসিট রিটার্ন ডিটেইলস (Implicit Return Nuances):
 * - যদি এক লাইনে লিখি, তবে মাঝখানে কোনো সেমিকোলন (;) দেওয়া যাবে না।
 * - যদি অবজেক্ট রিটার্ন করতে হয়, তবে সেটিকে প্রথম বন্ধনী () এর ভেতর রাখতে হবে।
 */

// অবজেক্ট রিটার্ন করার সঠিক নিয়ম (রিয়্যাক্টে এটি অনেক লাগে)
const getStudent = (id, name) => ({ id: id, name: name }); 

console.log(getStudent(101, "Hasan")); // { id: 101, name: "Hasan" }


/**
 * ৭. প্রফেশনাল টিপস (Rest vs Spread):
 * - ফাংশনের প্যারামিটারে যখন '...' ব্যবহার হয়, তখন একে বলে 'Rest Parameter'।
 * - এটি শুধু অ্যারো ফাংশন নয়, রেগুলার ফাংশনেও ব্যবহার করা যায়।
 * - এটি সবসময় প্যারামিটারের একদম শেষে থাকতে হয়।
 */

const showInfo = (title, ...tags) => {
    console.log("Title:", title);
    console.log("Tags:", tags); // এটি একটি অ্যারে প্রিন্ট করবে
};

showInfo("JS Learning", "functions", "arrow", "es6");


/**
 * ৮. রিয়্যাক্ট স্টাইল প্যারামিটার (Named Arguments using Object Destructuring):
 * ব্যাখ্যা: সাধারণত জাভাস্ক্রিপ্ট সিরিয়াল বা পজিশন অনুযায়ী ডাটা নেয়। 
 * কিন্তু আমরা যদি প্যারামিটারে অবজেক্ট ডিস্ট্রাকচারিং করি, তবে ডাটার সিরিয়াল 
 * আগে-পিছে হলেও কোনো সমস্যা নেই। এটি রিয়্যাক্ট প্রপস (Props) এর মতো কাজ করে।
 */

// অবজেক্ট হিসেবে প্যারামিটার গ্রহণ
const showProfile = ({ role, name, city = "Unknown" }) => { 
    // এখানে 'name' আগে বা 'role' পরে আসলো কিনা তাতে কিছু যায় আসে না
    console.log("Name:", name);
    console.log("Role:", role);
    console.log("City:", city);
};

// কল করার সময় অবজেক্ট পাঠানো (Named Arguments)
showProfile({ 
    role: "Frontend Developer", 
    name: "Hasan" 
}); 

// সুবিধা: 
// ১. অনেকগুলো প্যারামিটার থাকলে সিরিয়াল মনে রাখতে হয় না।
// ২. কোড পড়তে অনেক সহজ হয় (Self-documenting code)।
// ৩. সহজেই ডিফল্ট ভ্যালু (যেমন: city = "Unknown") সেট করা যায়।

//   সুবিধা (Pros):
//   ১. সিরিয়াল মনে রাখতে হয় না (No Positional dependency).
//   ২. কোড পড়লে বোঝা যায় কোন ডাটা কিসের (Self-documenting).
//   ৩. নতুন প্যারামিটার অ্যাড করা সহজ, আগের কোড ব্রেক করে না।
 

// উদাহরণ:
const createPost = ({ title, content, author }) => {
    console.log(`${title} by ${author}`);
};

// ডাটা আগে-পিছে পাঠালেও সমস্যা নেই
createPost({ author: "Hasan", title: "JS Tips", content: "..." });

/**
 * খারাপ দিক (Cons / Limitations):
 * ১. মেমোরি কস্ট: প্রতিবার ফাংশন কলের সময় একটি নতুন 'Object' তৈরি হয়, 
 * যা খুব বড় বা হাই-পারফরম্যান্স লুপে সামান্য মেমোরি চাপ দিতে পারে।
 * ২. টাইপিং ওভারহেড: প্রতিবার প্রপার্টির নাম (key) লিখতে হয়, যা কিছুটা সময়সাপেক্ষ।
 * ৩. সিনট্যাক্স ট্র্যাপ: রিসিভ করার সময় যদি ভুল করে কার্লি ব্র্যাকেট `{}` না দেওয়া হয়, 
 * তবে পুরো অবজেক্টটি প্রথম প্যারামিটারে ঢুকে যাবে এবং বাকিগুলো 'undefined' হবে।
 */

// ⚠️ সতর্কবার্তা (Caution):
const updateProfile = (name, age) => { /* ... */ }; 
updateProfile({ name: "Hasan", age: 25 }); 
// এখানে 'name' এর ভেতর পুরো {name: "Hasan", age: 25} অবজেক্টটি ঢুকে যাবে!