/**
 * ১. লজিক: কেন আমাদের Keys দরকার?
 * ----------------------------
 * রিয়্যাক্ট যখন কোনো লিস্ট রেন্ডার করে, সে তার মেমোরিতে (Virtual DOM) ওই লিস্টের 
 * একটি স্ন্যাপশট রাখে। যখন ডেটা চেঞ্জ হয়, সে নতুন স্ন্যাপশটের সাথে আগেরটার তুলনা করে। 
 * একে বলে 'Diffing Algorithm'।
 * * - Key ছাড়া: রিয়্যাক্ট বুঝতে পারে না কোন আইটেমটি নতুন আর কোনটি পুরনো। 
 * সে পুরো লিস্টকে নতুন করে তৈরি (Re-mount) করতে পারে, যা পারফরম্যান্স কমিয়ে দেয়।
 * - Key থাকলে: রিয়্যাক্ট এই ইউনিক আইডি দেখে সরাসরি ওই নির্দিষ্ট এলিমেন্টকে 
 * আপডেট বা মুভ করতে পারে।
 */

// ২. একটি আদর্শ লিস্ট রেন্ডারিং উদাহরণ
// ----------------------------------
const products = [
  { id: 'p1', name: 'Laptop', price: 50000 },
  { id: 'p2', name: 'Mouse', price: 1500 },
  { id: 'p3', name: 'Keyboard', price: 3000 },
];

function ProductList() {
  return (
    <ul>
      {products.map((product) => (
        // ১. Key সবসময় map() এর একদম শুরুর এলিমেন্টে দিতে হবে।
        // ২. Key হতে হবে ইউনিক এবং স্টেবল (যেমন: ডাটাবেসের ID)।
        <li key={product.id}>
          {product.name} - {product.price} BDT
        </li>
      ))}
    </ul>
  );
}



// ৩. INDEX AS KEY (কখন ব্যবহার করা যাবে আর কখন নয়)
// ----------------------------------------------
/**
 * অনেক সময় আমরা `(item, index) => ...` থেকে ইনডেক্সকে কি হিসেবে ব্যবহার করি।
 * এটি ব্যবহার করা যাবে কেবল তখনই যদি:
 * ১. লিস্টটি স্ট্যাটিক হয় (অর্থাৎ এর আইটেম কখনো বাড়বে বা কমবে না)।
 * ২. লিস্টটি কখনো ফিল্টার বা সর্ট করা হবে না।
 */

// কেন ইনডেক্স ব্যবহার করা বিপদজনক?
// ধরো তোমার লিস্টে ৩টি আইটেম আছে (Index 0, 1, 2)। 
// তুমি যদি একদম শুরুতে (Index 0) একটি নতুন আইটেম ঢোকাও, 
// তবে পুরনো সব আইটেমের ইনডেক্স ১ করে বেড়ে যাবে। 
// রিয়্যাক্ট ভাববে সব আইটেমই বদলে গেছে, ফলে সে পুরো লিস্ট আবার রেন্ডার করবে।



// ৪. KEYS-এর ৩টি গোল্ডেন রুল (হাসানের জন্য)
// --------------------------------------
/**
 * ১. UNIQUE: কি (Key) গুলোকে শুধু তার নিজের ভাই-বোনদের (Siblings) মধ্যে ইউনিক হতে হয়। 
 * পুরো অ্যাপে ইউনিক হওয়ার প্রয়োজন নেই।
 * ২. STABLE: কি (Key) যেন কখনো রেন্ডার হওয়ার সময় বদলে না যায়। 
 * (ভুল: key={Math.random()} - এটি করলে প্রতিবার পুরো UI নতুন করে তৈরি হবে)।
 * ৩. DO NOT PASS AS PROPS: কি (Key) রিয়্যাক্টের নিজস্ব প্রপার্টি। 
 * তুমি যদি কম্পোনেন্টের ভেতরে `props.key` দিয়ে এটি এক্সেস করতে চাও, তবে তা পাবে না। 
 * দরকার হলে আইডিটি আলাদা নামে (যেমন: id={product.id}) পাঠাতে হবে।
 */

