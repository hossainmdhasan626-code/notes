/**
 * ১. Logical AND (&&) - "The Short-circuit Way"
 * -------------------------------------------
 * এটি সবচেয়ে বেশি ব্যবহৃত হয় যখন আপনার শর্ত শুধু একটি: 
 * "যদি সত্য হয় তবে দেখাও, আর না হলে কিছুই দেখানোর দরকার নেই।"
 * * মেকানিজম: জাভাস্ক্রিপ্ট বাম পাশ থেকে চেক করে। বাম পাশ সত্য (True) হলে সে 
 * ডান পাশের JSX-এ যায়। কিন্তু বাম পাশ মিথ্যা (False) হলে সে ওখানেই থেমে যায়।
 */
const ShortCircuit = ({ hasMessage }) => {
    return (
        <div>
            {/* হাসান, খেয়াল রেখো বাম পাশে যেন শুধু boolean (true/false) থাকে */}
            {hasMessage && <p>আপনার একটি নতুন মেসেজ আছে!</p>}
        </div>
    );
};




/**
 * ২. Ternary Operator (? :) - "The Decision Maker"
 * ----------------------------------------------
 * যখন আপনার কাছে দুটি অপশন থাকে এবং একটিকে বেছে নিতে হবে।
 * সিনট্যাক্স: condition ? true_value : false_value
 */
const AuthStatus = ({ isLoggedIn }) => {
    return (
        <div>
            {/* যদিLoggedIn সত্য হয় তবে প্রথম বাটন, নাহলে দ্বিতীয় বাটন */}
            {isLoggedIn ? (
                <button>লগ আউট করুন</button>
            ) : (
                <button>লগ ইন করুন</button>
            )}
        </div>
    );
};


/**
 * ৩. if-else Statement - "The Logic Block"
 * ---------------------------------------
 * এটি JSX-এর ভেতরে সরাসরি লেখা যায় না। এটি রিটার্নের আগে লিখতে হয়।
 * যখন কন্ডিশন অনেক বড় হয় বা ডাটা লোড না হওয়া পর্যন্ত রেন্ডারিং বন্ধ রাখতে হয়।
 */
const ProductPage = ({ product, isLoading }) => {
    // ক. লোডিং স্টেট হ্যান্ডলিং
    if (isLoading) {
        return <div>লোড হচ্ছে...</div>;
    }

    // খ. ডাটা না থাকলে আর্লি রিটার্ন (Early Return)
    if (!product) {
        return null; // স্ক্রিনে কিছুই দেখাবে না
    }

    return (
        <div>
            <h1>{product.name}</h1>
            <p>{product.price} টাকা</p>
        </div>
    );
};


/**
 * ৪. Object Mapping - "The Scalable Way" ⚡
 * ---------------------------------------
 * যখন অনেকগুলো কন্ডিশন থাকে (যেমন ৫-৬টি আলাদা আলাদা স্ট্যাটাস)। 
 * if-else বা ternary দিয়ে করলে কোড নোংরা হয়ে যায়, তাই অবজেক্ট ব্যবহার করা বুদ্ধিমানের কাজ।
 */
const statusStyles = {
    pending: "⏳ অপেক্ষা করুন",
    shipped: "🚚 পাঠানো হয়েছে",
    delivered: "✅ পৌঁছে গেছে",
    cancelled: "❌ বাতিল করা হয়েছে"
};

const OrderStatus = ({ status }) => {
    // সরাসরি অবজেক্ট থেকে স্ট্যাটাসটি খুঁজে বের করে রেন্ডার করা
    return <div>অর্ডারের অবস্থা: {statusStyles[status] || "অজানা অবস্থা"}</div>;
};


/**
 * ৫. সুইচ কেস (Switch Case)
 * ------------------------
 * এটি অবজেক্ট ম্যাপিংয়ের মতোই, তবে যখন লজিক আরও জটিল হয় তখন এটি কাজে লাগে।
 */
const NotificationIcon = ({ type }) => {
    switch (type) {
        case 'info': return <span>ℹ️</span>;
        case 'warning': return <span>⚠️</span>;
        case 'error': return <span>🚫</span>;
        default: return null;
    }
};




/**
 * ==========================================================
 * হাসান'স গোল্ডেন চেক-লিস্ট (বিপদ থেকে বাঁচতে):
 * ==========================================================
 * ১. && এর বাম পাশে 0 (শূন্য) রাখবেন না। 
 * ভুল: {count && <p>Items: {count}</p>} -> স্ক্রিনে '0' প্রিন্ট হবে যদি count ০ হয়।
 * সঠিক: {count > 0 && <p>Items: {count}</p>} 
 * * ২. JSX-এর ভেতরে ( ) বা { } এর ব্যবহার ঠিক রাখুন।
 * * ৩. যদি কিছুই রেন্ডার করতে না চান, তবে সবসময় 'null' রিটার্ন করবেন। 
 * কারণ রিয়্যাক্ট 'null' পেলে ওই জায়গাটি খালি রাখে এবং কোনো এরর দেয় না।
 */

console.log("হাসান, কন্ডিশনাল রেন্ডারিং নোটটি এখন কমপ্লিট!");