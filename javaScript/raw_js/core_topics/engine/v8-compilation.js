/**
 * ৪৭. V8 ইঞ্জিনের অভ্যন্তরীণ কার্যপ্রণালী (The Pipeline)
 * -----------------------------------------------
 * লেখক: হাসান মাহমুদ (এনসাইক্লোপিডিয়া সিরিজ)
 * টেকনোলজি: Google V8 Engine (C++)
 */

// ধাপ ১: পার্সিং (Parsing) & AST তৈরি
// ---------------------------------
// কাজ: V8 প্রথমে তোমার Raw JS কোডকে 'টোকেন' এ ভাগ করে এবং একটি 'Abstract Syntax Tree' (AST) বানায়।
// কেন: কম্পিউটার সরাসরি তোমার টেক্সট ফাইল (let a = 10) পড়তে পারে না। AST হলো কোডের একটি লজিক্যাল ম্যাপ 
// যা থেকে ইঞ্জিন বুঝতে পারে কোনটা ভেরিয়েবল আর কোনটা ফাংশন।


// ধাপ ২: ইগনিশন ইন্টারপ্রিটার (Ignition Interpreter)
// ----------------------------------------------
// কাজ: AST পাওয়ার পর 'Ignition' এটাকে 'Bytecode'-এ রূপান্তর করে।
// বৈশিষ্ট্য: বাইটকোড হলো একটি ইন্টারমিডিয়েট (মধ্যবর্তী) কোড। এটি জাভাস্ক্রিপ্টের চেয়ে অনেক ফাস্ট 
// কিন্তু একদম মেশিন কোড (0101) নয়। 
// কেন এটি দরকার: সরাসরি মেশিন কোড বানালে সেটা অনেক বেশি মেমোরি (RAM) দখল করে। 
// বাইটকোড হালকা এবং সাথে সাথেই রান করা শুরু করতে পারে।

// ধাপ ৩: প্রোফাইলার (The Profiler / Monitor)
// ----------------------------------------
// কাজ: বাইটকোড যখন রান হয়, তখন প্রোফাইলার নজর রাখে কোন ফাংশনগুলো বারবার কল হচ্ছে।
// হট ফাংশন (Hot Functions): যদি কোনো ফাংশন বারবার চলে, প্রোফাইলার সেটাকে 'Hot' হিসেবে চিহ্নিত করে।
// উদাহরণ: একটি লুপের ভেতর থাকা ক্যালকুলেশন।

// ধাপ ৪: টার্বোফ্যান কম্পাইলার (TurboFan - The Optimizer)
// ---------------------------------------------------
// কাজ: প্রোফাইলার যখন কোনো 'Hot Function' পায়, সে সেটাকে 'TurboFan' কম্পাইলারের কাছে পাঠায়।
// ম্যাজিক: TurboFan ওই বাইটকোডকে সরাসরি 'Highly Optimized Machine Code' (Binary: 0101) এ রূপান্তর করে।
// ফলাফল: এর পর থেকে ওই ফাংশনটা আর ইন্টারপ্রিট করতে হয় না, সরাসরি CPU-তে বিদ্যুতের গতিতে রান হয়।


// ধাপ ৫: ডি-অপ্টিমাইজেশন (De-optimization / Speculative Bailout)
// ----------------------------------------------------------
// কাজ: জাভাস্ক্রিপ্ট যেহেতু ডাইনামিক, তাই যদি কোনো ফাংশনের ডাটা টাইপ হঠাৎ বদলে যায় (যেমন: 
// আগে Number যোগ করছিল, এখন String পাঠালে), TurboFan তখন ওই মেশিন কোড বাতিল করে দেয়।
// রেজাল্ট: ইঞ্জিন আবার নিরাপদ 'Bytecode' বা ইন্টারপ্রিটার মোডে ফিরে আসে।

/**
 * হাসান'স গোল্ডেন নোটস:
 * ১. JIT (Just-In-Time): V8 রান-টাইমে কোড কম্পাইল করে, তাই একে JIT কম্পাইলার বলে।
 * ২. No Files: এই পুরো প্রসেসটি র‍্যামের (RAM) ভেতরে ঘটে, কোনো আলাদা .exe বা .bin ফাইল তৈরি হয় না।
 * ৩. C++ Power: V8 নিজে C++ এ লেখা বলেই সে সেকেন্ডের ভগ্নাংশের মধ্যে এই বিশাল রূপান্তর করতে পারে।
 */

/**
 * ৪৭. জাভাস্ক্রিপ্টের রূপান্তর যাত্রা (The Lifecycle of JS in V8)
 * --------------------------------------------------------
 * এই চার্টটি দেখায় কীভাবে একটি টেক্সট কোড CPU-র জন্য মেশিন কোডে পরিণত হয়।
 */

/*
  [১. SOURCE CODE] -> (তুমি যা লিখলে: function sum(a,b) { return a+b })
         |
         v
  [২. PARSER & SCANNER] -> (কোডকে টোকেনে ভাগ করে এবং ব্যাকরণ চেক করে)
         |
         v
  [৩. AST (Abstract Syntax Tree)] -> (কোডের একটি লজিক্যাল বৃক্ষ বা ম্যাপ)
         |
         v
  [৪. IGNITION INTERPRETER] -> (AST থেকে 'Bytecode' তৈরি করে যা দ্রুত রান হয়)
         |
         |--------------------<--------------------|
         v                                         |
  [৫. BYTECODE EXECUTION]                          | (De-optimization: টাইপ বদলে গেলে)
         |                                         |
         v (Profiler নজর রাখছে)                    |
  [৬. HOT FUNCTIONS IDENTIFIED]                    |
         |                                         |
         v (TurboFan এ পাঠানো হলো)                 |
  [৭. TURBOFAN COMPILER] -> (বাইটকোডকে 'Optimized Machine Code' এ রূপান্তর)
         |
         v
  [৮. MACHINE CODE (0101)] -> (সরাসরি CPU-তে বিদ্যুতের গতিতে এক্সিকিউট হয়)
*/



/**
 * প্রতিটি ধাপের গভীরে (Detailed Breakdown):
 * * ১. AST (Abstract Syntax Tree): 
 * এটি হলো কোডের কঙ্কাল। V8 সরাসরি স্ট্রিং পড়তে পারে না, তাই এই ট্রি-এর মাধ্যমে 
 * সে বোঝে কোন অপারেশন আগে হবে আর কোনটা পরে।
 * * ২. Bytecode (The Middleman): 
 * ইগনিশন (Ignition) যখন বাইটকোড বানায়, তখন সে মেমোরি বাঁচায়। মেশিন কোড অনেক বড় হয়, 
 * কিন্তু বাইটকোড ছোট এবং মেমোরি-সাশ্রয়ী।
 * * ৩. TurboFan (The Speed Demon): 
 * এটি একটি JIT (Just-In-Time) কম্পাইলার। সে শুধু সেই কোডগুলোকেই মেশিন কোড বানায় 
 * যেগুলো বারবার চলে। এর ফলে ব্রাউজার অনেক কম লোডে অনেক বেশি স্পিড পায়।
 * * ৪. Web IDL (The Bridge): 
 * যখন এই মেশিন কোডগুলো DOM-এর সাথে কথা বলতে চায় (যেমন: একটা div-এর রঙ বদলানো), 
 * তখন তারা Web IDL সেতু ব্যবহার করে Blink ইঞ্জিনের C++ অবজেক্টের সাথে যোগাযোগ করে।
 */



