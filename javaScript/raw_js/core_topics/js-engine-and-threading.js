/**
 * ২. V8 ইঞ্জিন এবং কনভার্সন (From JS to Machine Code)
 * -------------------------------------------------
 * V8 ইঞ্জিন (C++ দিয়ে তৈরি) সরাসরি JS বোঝে না। সে নিচের ধাপে কাজ করে:
 * * Step A: Parsing - কোড পড়ে AST (Abstract Syntax Tree) তৈরি করে।
 * Step B: Ignition (Interpreter) - AST থেকে Bytecode তৈরি করে।
 * Step C: TurboFan (JIT Compiler) - বাইটকোডকে সরাসরি Machine Code (0, 1) এ রূপান্তর করে।
 * Step D: Execution - প্রসেসর সেই ০ আর ১ রান করে।
 */



