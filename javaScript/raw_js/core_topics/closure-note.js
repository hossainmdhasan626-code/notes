// ৩. অ্যাডভান্সড ক্লোজার (Data Privacy)
// ----------------------------------------------------------
// ক্লোজার ব্যবহার করে আমরা "প্রাইভেট ভ্যারিয়েবল" তৈরি করতে পারি যা বাইরে থেকে কেউ চেঞ্জ করতে পারবে না।

function createCounter() {
    let count = 0; // এটি বাইরে থেকে সরাসরি এক্সেসযোগ্য নয়

    return {
        increment: function() {
            count++;
            console.log("Current count:", count);
        },
        decrement: function() {
            count--;
            console.log("Current count:", count);
        }
    };
}

const myCounter = createCounter();
myCounter.increment(); // ১
myCounter.increment(); // ২
