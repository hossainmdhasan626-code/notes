// উদাহরণ ১: সাধারণ ক্লোজার
function greeting() {
    let message = "আসসালামু আলাইকুম"; // বাইরের ভ্যারিয়েবল

    return function() {
        console.log(message); // এই ইনার ফাংশনটি 'message' কে মনে রেখেছে
    };
}

const sayHello = greeting();
sayHello(); // আউটপুট: আসসালামু আলাইকুম

// উদাহরণ ২: ডাইনামিক ক্লোজার
function createMultiplier(factor) {
    return function(number) {
        return number * factor; // 'factor' ভ্যারিয়েবলটি ক্লোজারের মাধ্যমে আসছে
    };
}

const double = createMultiplier(2);
console.log(double(5)); // ১০
const triple = createMultiplier(3);
console.log(triple(5)); // ১৫


