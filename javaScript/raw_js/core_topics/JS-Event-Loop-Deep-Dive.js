/**
 * বেসিক কনসেপ্ট: ইভেন্ট লুপ আসলে কী?
 * -----------------------------------
 * জাভাস্ক্রিপ্ট হলো 'Single Threaded', অর্থাৎ সে একবারে একটির বেশি কাজ করতে পারে না।
 * কিন্তু ব্রাউজারে আমরা একসাথে অনেক কাজ (যেমন: টাইমার, এপিআই কল) হতে দেখি।
 * এই অসম্ভবকে সম্ভব করে 'Event Loop'। এটি একটি নিরন্তর লুপ যা ব্যাকগ্রাউন্ডে চেক করে:
 * "স্ট্যাক কি খালি? কিউ-তে কি কোনো কাজ আছে?"
 */

/**
 * ১. THE INFINITY LOOP (ইভেন্ট লুপের মূল চরিত্র)
 * -------------------------------------------
 * ইভেন্ট লুপ হলো একটি 'while(true)' লুপ। এটি ততক্ষণ পর্যন্ত চলতেই থাকে যতক্ষণ
 * ব্রাউজারের ট্যাবটি ওপেন থাকে। এটি কোনো ম্যাজিক নয়, এটি সারাক্ষণ নিচের কাজগুলো করে:
 * - কল স্ট্যাক (Call Stack) চেক করে।
 * - কিউ (Queue) গুলোর ভেতর উঁকি দিয়ে দেখে কোনো কাজ পড়ে আছে কি না।
 * - যদি কাজ থাকে, তবে সেগুলোকে স্ট্যাকে তুলে এনে রান করায়।
 */

// ২. ৪টি প্রধান কিউ (Queues) এবং তাদের সিরিয়াল
// ------------------------------------------
/**
 * ব্রাউজারে মূলত ৪টি কিউ থাকে। ইভেন্ট লুপ নিচের সিরিয়াল অনুযায়ী এদের চেক করে:
 * * ১. Microtask Queue (VIP Queue) - [সবচেয়ে বেশি অগ্রাধিকার]
 * ২. Animation Frame Queue (Render Queue এর অংশ)
 * ৩. Render Queue (UI Updates)
 * ৪. Callback Queue / Task Queue (Commoners) - [সবচেয়ে কম অগ্রাধিকার]
 */

// ৩. কিউগুলোর বিস্তারিত ব্যবচ্ছেদ (Deep Dive)
// ---------------------------------------

/**
 * ক) MICROTASK QUEUE (The VIP Lane):
 * ----------------------------------
 * - কাজ: Promise-এর রেজাল্ট প্রসেস করা (resolve/reject), async/await হ্যান্ডেল করা।
 * - কেন এটি আলাদা? এটি অন্য কিউগুলোর মতো নয়। ইভেন্ট লুপের কড়া নিয়ম হলো—
 * স্ট্যাক খালি হওয়ার পর এই কিউতে যতগুলো কাজ (১০টা বা ১০০টা) থাকুক না কেন,
 * সবগুলো একবারে শেষ না করে সে পরবর্তী ধাপে যাবে না।
 * - রিস্ক: যদি এই কিউতে অসীম কাজ দাও, তবে ব্রাউজার কোনোদিন ছবি (Render) আঁকতে পারবে না।
 */

/**
 * খ) ANIMATION FRAME QUEUE (rAF):
 * ------------------------------
 * - কাজ: requestAnimationFrame() ফাংশনগুলো এখানে জমা হয়।
 * - ডিটেইলস: তুমি যদি কোনো এলিমেন্টকে স্মুথভাবে মুভ করাতে চাও (যেমন: স্লাইডার বা গেম),
 * তবে এই কিউ ব্যবহার করা হয়।
 * - স্পেশালিটি: এটি রেন্ডার কিউ-এর ঠিক আগে রান হয়। ব্রাউজার যখনই দেখে যে তাকে
 * স্ক্রিন আপডেট করতে হবে, সে আগে এই কিউ-এর সব কাজ শেষ করে।
 */

/**
 * গ) RENDER QUEUE (The Screen Painter):
 * -------------------------------------
 * - কাজ: এটি মূলত ৩টি ধাপের সমষ্টি:
 * ১. requestAnimationFrame: অ্যানিমেশনের জন্য সবচেয়ে পারফেক্ট সময়।
 * ২. Layout/Reflow: এলিমেন্টের সাইজ এবং পজিশন ক্যালকুলেট করা।
 * ৩. Paint: পিক্সেলগুলোকে মনিটরে ফুটিয়ে তোলা।
 * - মেকানিজম: ইভেন্ট লুপ যখন ঘুরে বেড়ায়, সে প্রতিবার এখানে ঢোকে না।
 * সে চেক করে: "এখন কি ১৬.৬ms পার হয়েছে?" (যদি স্ক্রিন ৬০Hz হয়)।
 * যদি সময় হয়ে থাকে, তবেই সে রেন্ডার কিউ-তে ঢোকে এবং ইউজারকে নতুন ছবি দেখায়।
 */

/**
 * ঘ) CALLBACK / MACRO-TASK QUEUE (The Waiting Room):
 * --------------------------------------------------
 * - কাজ: setTimeout, setInterval, fetch-এর রেসপন্স আসা, মাউস ক্লিক বা স্ক্রল ইভেন্ট।
 * - নিয়ম: এটি সবচেয়ে কম পাওয়ারফুল। রেন্ডারিং শেষ হওয়ার পর যদি ইভেন্ট লুপ দেখে
 * তার হাতে অফুরন্ত সময় আছে, তখনই সে এখান থেকে কাজ নেয়।
 * - মজার নিয়ম: ইভেন্ট লুপ এই কিউ থেকে একবারে মাত্র "একটি" কাজ স্ট্যাকে পাঠায়,
 * তারপর সে আবার ওপরের কিউগুলো (Microtask, Render) চেক করতে চলে যায়।
 */

// ৪. ইভেন্ট লুপের "লুপ" মেকানিজম (The Execution Flow)
// -------------------------------------------------
/**
 * হাসান, ইভেন্ট লুপের চক্রটা কল্পনা করো এভাবে:
 * * ১. কল স্ট্যাকের সব কাজ শেষ করো (যেমন: console.log, calculation)।
 * ২. মাইক্রোটাস্ক কিউ-তে যাও। কিউ খালি না হওয়া পর্যন্ত সব কাজ শেষ করো।
 * ৩. এখন কি রেন্ডার করার সময় হয়েছে? (১৬.৬ms বা ৩২ms?)
 * - হ্যাঁ? তবে রেন্ডার কিউ-তে ঢোকো এবং স্ক্রিন আপডেট করো।
 * - না? তবে এই ধাপটা স্কিপ করো।
 * ৪. কলব্যাক কিউ থেকে মাত্র "একটি" কাজ হাতে নাও এবং কল স্ট্যাকে পাঠাও।
 * ৫. আবার ১ নম্বর ধাপে ফিরে যাও (Infinity Loop)।
 */

/**
 * ৫. ব্রাউজার স্লিপ মোড (Smart Polling):
 * -----------------------------------
 * - লজিক্যালি এটি একটি Infinite Loop, কিন্তু এটি CPU-কে তপ্ত করে না। 
 * - যখন কোনো কিউতেই কাজ থাকে না, তখন ইভেন্ট লুপ "Block" বা "Wait" স্টেটে চলে যায়। 
 * - একে বলা হয় 'Semaphores' বা 'Message Loop' মেকানিজম। 
 * - ব্রাউজার তখন অপারেটিং সিস্টেম থেকে একটি সিগন্যালের জন্য অপেক্ষা করে। 
 * - যেমনই তুমি মাউস নাড়াও বা ২ সেকেন্ডের টাইমার শেষ হয়, অমনি OS ইভেন্ট লুপকে 
 * একটি 'Wake Up' কল দেয় এবং লুপটি আবার এক নম্বর ধাপ থেকে ঘোরা শুরু করে।
 */

// ৬. ছোট ছোট কিছু "গোপন" ডিটেইলস (Interview Special):
// -----------------------------------------------

/**
 * ৬.১. Starvation (অনাহারে রাখা):
 * যদি তুমি অসীমভাবে একটার পর একটা Promise তৈরি করতেই থাকো, তবে Microtask Queue
 * কখনোই খালি হবে না। ফলে ইভেন্ট লুপ কখনো Render Queue-তে যেতে পারবে না এবং
 * তোমার ব্রাউজার স্ক্রিন "হ্যাং" হয়ে যাবে। একে বলে 'Render Starvation'।
 * * ৬.২. setTimeout(0) এর রহস্য:
 * ০ সেকেন্ড দিলেও এটি কেন পরে রান হয়? কারণ এটি Task Queue-তে যায়।
 * ইভেন্ট লুপ আগে সব Synchronous কাজ এবং সব Microtask শেষ করে,
 * তারপর Task Queue থেকে একে তুলে আনে।
 * * ৬.৩. Idle Callback Queue (৫ম কিউ):
 * ব্রাউজারে 'requestIdleCallback' নামে আরও একটি কিউ আছে। যখন ইভেন্ট লুপের
 * হাতে একদমই কোনো কাজ থাকে না (Idle), তখন সে এই কিউ-এর ছোটখাটো কাজগুলো করে।
 */

// ৭. একটি প্র্যাক্টিক্যাল উদাহরণ (Hasan's Visualization):
// ----------------------------------------------------

console.log("১. স্ট্যাক"); // সরাসরি স্ট্যাকে

setTimeout(() => {
  console.log("৪. টাস্ক কিউ"); // ৪ নম্বর সিরিয়ালে যাবে
}, 0);

Promise.resolve().then(() => {
  console.log("২. মাইক্রোটাস্ক"); // ২ নম্বর সিরিয়ালে যাবে
});

requestAnimationFrame(() => {
  console.log("৩. রেন্ডার কিউ"); // ৩ নম্বর সিরিয়ালে যাবে
});

console.log("৫. স্ট্যাক শেষ"); // স্ট্যাকের কাজ

