// ১. JS ENGINE WORKFLOW
// --------------------
// JS Code -> Browser (V8) -> Machine Code (0101) -> Computer Output
// অনুবাদ করার ৩টি উপায়: 
// ১. Interpretation (লাইন বাই লাইন, দ্রুত শুরু হয়)
// ২. Compilation (একবারে পুরোটা, পরে ফাস্ট চলে)
// ৩. JIT (Just-In-Time): আধুনিক V8 দুটোর মিশ্রণ ব্যবহার করে (Fast enough!)

// ২. EXECUTION CONTEXT (EC) - "কোড রান করার পরিবেশ"
// ----------------------------------------------
// যখনই JS ফাইল রান হয়, সবার আগে তৈরি হয় Global Execution Context (GEC)।
// প্রতিটি EC-এর ২টি ধাপ বা Phase থাকে:

/**
 * PHASE 1: Memory Phase (একে আমরা Hoisting বলি)
 * -------------------------------------------
 * এই ধাপে কোড রান হয় না, শুধু মেমোরিতে জায়গা বরাদ্দ হয়।
 * - var vName : undefined (মেমোরিতে জায়গা পায়, মান থাকে undefined)
 * - function fName : { পুরো বডি } (মেমোরিতে ফাংশনটি আস্ত কপি হয়ে থাকে)
 * - let / const : (Uninitialized/TDZ) (মেমোরিতে জায়গা পায়, কিন্তু তালা মারা থাকে)
 */

/**
 * PHASE 2: Execution Phase
 * ------------------------
 * এই ধাপে ভ্যালু অ্যাসাইন হয় এবং ফাংশন কল হয়।
 */

// ৩. CALL STACK (EXECUTION STACK) - "সিরিয়াল মেইনটেইন করা"
// ---------------------------------------------------
// স্ট্যাকের নিয়ম: LIFO (Last In, First Out) - যে সবার শেষে আসে, সে সবার আগে বের হয়।

var a = 1;

function one() {
  console.log(a); // ১. সে নিজের ঘরে 'a' না পেয়ে নিচে (GEC) উঁকি দিবে
  
  function two() {
    var b = 2;
    console.log(b); 
    
    function three(d) {
      let c = 3;
      console.log(c + d); // ২. 'c' নিজের ঘরে পেল, 'd' আর্গুমেন্টে পেল।
    }
    
    three(4); // three() এর জন্য নতুন FEC তৈরি হলো এবং স্ট্যাকের উপরে বসলো
  }
  
  two(); // two() এর FEC স্ট্যাকের উপরে বসলো
}

one(); // one() এর FEC স্ট্যাকের উপরে বসলো

// ৪. SCOPE CHAIN: কেন চাইল্ড প্যারেন্টের ডেটা পায়, কিন্তু প্যারেন্ট চাইল্ডের পায় না?
// -------------------------------------------------------------------------
/**
 * হাসান, তোমার স্ক্রিনশট এবং নোটের ব্যাখ্যাটি এখানে একদম পানির মতো পরিষ্কার:
 * * কেন চাইল্ড পায় (Bottom-to-Top Access):
 * যখন 'three()' ফাংশনটি চলে, তার FEC স্ট্যাকের সবচেয়ে উপরে থাকে। 
 * তার নিচে 'two()', তার নিচে 'one()', আর সবার নিচে 'GEC' থাকে। 
 * চাইল্ড যখন নিজের ঘরে কোনো ভেরিয়েবল পায় না, সে তার নিচে থাকা (Lexical Parent) 
 * কনটেক্সটের মেমোরিতে উঁকি দেওয়ার পারমিশন পায়। একেই বলে Scope Chain।
 * * কেন প্যারেন্ট পায় না (Unreachable Memory):
 * ১. যখন 'three()' এর কাজ শেষ হয়, সে স্ট্যাক থেকে 'Pop Out' বা ডিলিট হয়ে যায়। 
 * ২. 'two()' যখন আবার সচল হয়, তখন 'three()' এর মেমোরি বক্সটাই তো পৃথিবীতে নেই! 
 * ৩. মেমোরি থেকে মুছে যাওয়া কোনো কিছু প্যারেন্ট কোনোভাবেই অ্যাক্সেস করতে পারে না।
 */

// ৫. ছোট ছোট কিছু মিসিং ডিটেইলস (যা তোমার নোটে এড করা দরকার):
// --------------------------------------------------------
/**
 * ১. Arguments Object: প্রতিটি Function Context (FEC) তৈরি হওয়ার সময় 'arguments' 
 * নামক একটি স্পেশাল অবজেক্ট তৈরি হয় (যা তোমার স্ক্রিনশটে {0: 4} হিসেবে দেখাচ্ছিল)।
 * এটি ফাংশনে পাঠানো সব ইনপুটকে ধরে রাখে।
 * * ২. Return Statement: যখনই কোনো ফাংশন 'return' পায়, অমনি ওই FEC স্ট্যাক থেকে 
 * ডিলিট হওয়ার প্রক্রিয়া শুরু করে। যদি return না থাকে, তবে ডিফল্টভাবে 'undefined' 
 * রিটার্ন করে সে ডিলিট হয়ে যায়।
 * * ৩. this: প্রতিটি কনটেক্সটে একটি 'this' কি-ওয়ার্ড তৈরি হয়। গ্লোবাল কনটেক্সটে 
 * এটি 'window' অবজেক্টকে পয়েন্ট করে।
 */

