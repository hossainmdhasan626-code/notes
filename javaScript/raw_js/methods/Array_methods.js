// ---------------------------------------------------------
// ১. at() [ES2022]
// বিবরণ: ইনডেক্স দিয়ে আইটেম খুঁজে বের করে। নেগেটিভ ইনডেক্স সাপোর্ট করে।
// ---------------------------------------------------------
const fruits = ["Apple", "Mango", "Banana"];
console.log(fruits.at(0));  // "Apple"
console.log(fruits.at(-1)); // "Banana" (শেষ দিক থেকে প্রথমটি)
// খুঁটিনাটি: fruits[-1] দিলে undefined আসে, কিন্তু at(-1) দিলে শেষ আইটেম পাওয়া যায়।


// ---------------------------------------------------------
// ২. copyWithin() [ES5.1]
// বিবরণ: অ্যারের এক অংশ কপি করে অন্য পজিশনে পেস্ট করে। অরিজিনাল অ্যারে বদলে যায়।
// ---------------------------------------------------------
const arr1 = [1, 2, 3, 4, 5];
console.log(arr1.copyWithin(0, 3)); // [4, 5, 3, 4, 5] (৩ নং ইনডেক্স থেকে শেষ পর্যন্ত কপি করে ০ তে বসানো হয়েছে)
const arr2 = [1, 2, 3, 4, 5];
console.log(arr2.copyWithin(1, 3, 4)); // [1, 4, 3, 4, 5] (৩ থেকে ৪ এর আগ পর্যন্ত কপি করে ১ এ বসানো হয়েছে)
// খুঁটিনাটি: এটি অ্যারের লেন্থ পরিবর্তন করে না, শুধু ওভাররাইট করে।


// ---------------------------------------------------------
// ৩. flat() [ES2019]
// বিবরণ: নেস্টেড অ্যারেকে ভেঙে সাধারণ অ্যারে করে দেয়।
// ---------------------------------------------------------
const nest = [1, [2, 3], [4, [5]]];
console.log(nest.flat());    // [1, 2, 3, 4, [5]] (বাই ডিফল্ট ১ লেভেল)
console.log(nest.flat(2));   // [1, 2, 3, 4, 5] (২ লেভেল গভীর পর্যন্ত ভাঙবে)
// খুঁটিনাটি: এটি অ্যারের ভেতরের খালি জায়গা (empty slots) রিমুভ করে দেয়।


// ---------------------------------------------------------
// ৪. flatMap() [ES2019]
// বিবরণ: প্রথমে map করে তারপর ১ লেভেল flat করে। 
// ---------------------------------------------------------
const words = ["Hello World", "JS is Fun"];
console.log(words.flatMap(str => str.split(" "))); // ["Hello", "World", "JS", "is", "Fun"]
console.log([1, 2, 3].flatMap(x => [x * 2])); // [2, 4, 6]
// খুঁটিনাটি: এটি map().flat(1) এর চেয়ে পারফরম্যান্সের দিক থেকে দ্রুত।


// ---------------------------------------------------------
// ৫. slice() [Legacy/ES3]
// বিবরণ: নির্দিষ্ট অংশ কপি করে নতুন অ্যারে দেয়। অরিজিনাল অ্যারে ঠিক থাকে।
// ---------------------------------------------------------
const items = ["A", "B", "C", "D"];
console.log(items.slice(1, 3)); // ["B", "C"] (১ থেকে ৩ এর আগ পর্যন্ত)
console.log(items.slice(-2));   // ["C", "D"] (শেষের ২ টি)
// খুঁটিনাটি: এটি "Immutable" মেথড, রিয়্যাক্টে কপি বানাতে খুব লাগে।


// ---------------------------------------------------------
// ৬. splice() [Legacy/ES3]
// বিবরণ: আইটেম ডিলিট বা অ্যাড করে। অরিজিনাল অ্যারে বদলে দেয়।
// ---------------------------------------------------------
const colors = ["Red", "Green", "Blue"];
colors.splice(1, 1, "Yellow"); // ১ নম্বর ইনডেক্স থেকে ১টি ডিলিট করে Yellow বসাও
console.log(colors); // ["Red", "Yellow", "Blue"]
const deleted = colors.splice(0, 1); // ০ নম্বর থেকে ১টি ডিলিট
console.log(deleted); // ["Red"] (যা ডিলিট হয়েছে তা রিটার্ন করে)
// খুঁটিনাটি: ২য় প্যারামিটারটি ইনডেক্স নয়, বরং কয়টি আইটেম কাটবেন তার পরিমাণ।


// ---------------------------------------------------------
// ৭. toSpliced() [ES2023]
// বিবরণ: splice() এর মতো কাজ করে কিন্তু অরিজিনাল অ্যারেকে না বদলে নতুন কপি দেয়।
// ---------------------------------------------------------
const list = [1, 2, 3];
const newList = list.toSpliced(1, 1, 10); 
console.log(newList); // [1, 10, 3]
console.log(list);    // [1, 2, 3] (অরিজিনাল ঠিক আছে)


// ---------------------------------------------------------
// ৮. sort() & toSorted() [ES2023 for toSorted]
// বিবরণ: অ্যারেকে সাজায়। sort অরিজিনাল বদলে ফেলে, toSorted কপি দেয়।
// ---------------------------------------------------------
const nums = [10, 2, 100, 1];
console.log(nums.sort((a, b) => a - b)); // [1, 2, 10, 100] (নাম্বার সর্টিং লজিক a-b)
const names = ["Zayan", "Abir", "Hasan"];
console.log(names.sort((a, b) => a.localeCompare(b))); // ["Abir", "Hasan", "Zayan"]
// খুঁটিনাটি: স্ট্রিং সর্টে localeCompare() ছাড়া বড় হাতের অক্ষর আগে চলে আসতে পারে।


// ---------------------------------------------------------
// ৯. map() & filter() [ES6]
// বিবরণ: map নতুন অ্যারে বানায়, filter শর্ত অনুযায়ী ডাটা ছাঁটাই করে।
// ---------------------------------------------------------
const data = [10, 20, 30];
console.log(data.map(x => x + 5)); // [15, 25, 35]
console.log(data.filter(x => x > 15)); // [20, 30]
// খুঁটিনাটি: map সবসময় অরিজিনাল অ্যারের সমান লেন্থের অ্যারে দেয়।


// ---------------------------------------------------------
// ১০. reduce() [ES6]
// বিবরণ: পুরো অ্যারের ডাটা এক জায়গায় জমা করে (Accumulate)।
// ---------------------------------------------------------
const cart = [100, 200, 300];
const total = cart.reduce((acc, curr) => acc + curr, 0); 
console.log(total); // 600
const max = cart.reduce((a, b) => Math.max(a, b)); // ৩০০ (সর্বোচ্চ বের করা)
// খুঁটিনাটি: দ্বিতীয় প্যারামিটার '0' হলো শুরুর মান। এটি না দিলে ১ম আইটেমকে শুরুর মান ধরে।


// ---------------------------------------------------------
// ১১. find() & findIndex() [ES6]
// ---------------------------------------------------------
const users = [{id: 1, name: "Hasan"}, {id: 2, name: "Ali"}];
console.log(users.find(u => u.id === 1)); // {id: 1, name: "Hasan"}
console.log(users.findIndex(u => u.name === "Ali")); // 1
// খুঁটিনাটি: find() প্রথম ম্যাচ পাওয়া মাত্রই থামা যায়। কিছু না পেলে undefined দেয়।


// ---------------------------------------------------------
// ১২. includes(), some(), every() [ES2016/ES6]
// ---------------------------------------------------------
const ages = [18, 20, 25];
console.log(ages.includes(20)); // true
console.log(ages.some(age => age > 24)); // true (অন্তত একজন আছে)
console.log(ages.every(age => age > 15)); // true (সবাই ১৫ এর বড়)


// ---------------------------------------------------------
// ১৩. with() [ES2023]
// বিবরণ: ইনডেক্স অনুযায়ী ভ্যালু বদলে নতুন অ্যারে দেয় (ইমিউটেবল)।
// ---------------------------------------------------------
const arrWith = ["A", "B", "C"];
console.log(arrWith.with(1, "Z")); // ["A", "Z", "C"]


// ---------------------------------------------------------
// ১৪. Static Methods: Array.isArray() & Array.from()
// ---------------------------------------------------------
console.log(Array.isArray([1, 2])); // true
console.log(Array.from("ABC")); // ["A", "B", "C"] (স্ট্রিং বা অবজেক্ট থেকে অ্যারে বানায়)